public class AssignmentOp {
    public static void main(String[] args) {
        int a=10;//here "=" is one of the assignment operator
        System.out.println(a += 1);
        System.out.println(a -= 1);
        System.out.println(a *= 2);
        System.out.println(a /= 2);
        System.out.println(a %= 2);
    }
}
